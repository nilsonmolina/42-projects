int		ft_sqrt(int nb)
{
	int sqr;

	if (nb > 46340 * 46340)
		return (0);
	sqr = 0;
	while (sqr * sqr < nb)
		sqr++;
	if (sqr * sqr == nb)
		return (sqr);
	else
		return (0);
}
